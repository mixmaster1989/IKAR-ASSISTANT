        # –ë–µ—Ä–µ–º —Å–∞–º—É—é –±–æ–ª—å—à—É—é —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏—é (–ø–æ—Å–ª–µ–¥–Ω—é—é –≤ —Å–ø–∏—Å–∫–µ)
        photo = photos[-1]
        file_id = photo.get("file_id")
        
        if not file_id:
            logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å file_id —Ñ–æ—Ç–æ–≥—Ä–∞—Ñ–∏–∏: {photo}")
