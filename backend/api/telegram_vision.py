image_analyzer = None

def get_image_analyzer() -> ImageAnalyzer:
    """–ü–æ–ª—É—á–∞–µ—Ç –∏–ª–∏ —Å–æ–∑–¥–∞–µ—Ç —ç–∫–∑–µ–º–ø–ª—è—Ä –∞–Ω–∞–ª–∏–∑–∞—Ç–æ—Ä–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π."""
    global image_analyzer
    if image_analyzer is None:
        image_analyzer = ImageAnalyzer()
    return image_analyzer

