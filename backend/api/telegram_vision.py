        logger.info(f"[VISION] –í–æ–∑–≤—Ä–∞—â–∞—é –æ–ø–∏—Å–∞–Ω–∏–µ: {description[:100]}...")
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –æ–ø–∏—Å–∞–Ω–∏–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è
